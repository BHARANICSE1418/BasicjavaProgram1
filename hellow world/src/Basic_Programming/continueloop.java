package Basic_Programming;

public class continueloop {

	public static void main(String[] args) {
		
		for(int i=1;i<=50;i++)
		{  
	        if(i==7)
	        {  
	        	System.out.print("Continue Statement ");
	            continue; 
	        }  
	        System.out.print(i+" ");  
	    }  
	}

}
