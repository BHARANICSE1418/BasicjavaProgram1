package Basic_Programming;

public class opeartors {

	public static void main(String[] args) {
		//num++ first returns and then applies +1 operation. 
		//++num first adds +1 and then returns incremented value.
		
		  int num0 =1;
		  System.out.print(num0);
		  int num1=1;
		  System.out.print(num1++);
		  int num2=1;
		  System.out.print(++num2);
		  
		  int n0 =5;
		  System.out.print(n0);
		  int n1=5;
		  System.out.print(n1--);
		  int n2=5;
		  System.out.print(--n2);		

	
	}

}
